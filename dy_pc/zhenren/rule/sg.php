                    	<li>三公以牌面点数的总和来比较大小，加上公牌(J，Q，K)来变化大小牌型。</li>
                    	<li><span class="style4"> 公牌及10点以「0」点计，其它牌则以牌面点数来计算，总和超过10或20则取其个位数字来看。</span></li>
                    	<li>公牌以「P」来代表，1张公牌显示为「P」，2张公牌显示为「2P」，3张公牌（三公）显示为「3P」。</li>
                    	<li>牌面的显示方法为「公牌+点数」，例如拿到1张公牌与点数和为8，则显示「P8」，2张公牌与点数和为0，则显示「2P0」。</li>
                    	<li>最大牌型为「三公」，其次为「双公9」；牌型中若无公牌，则最大牌型为9点，扑克牌的花色无任何影响。</li>
                    	<li>双方点数与公牌数相同时则为和，出现和牌时，会员若下注「输」或「赢」则退回注额。</li>
