于翻倍中下注时，会员的【可用余额】，必须高于【投注分数】的5倍<font color="red">（投注分数 X 5公赔率）</font>，请多加注意。<br />
于投注平倍时，不在此限。<br />
<br />
<b>范例：</b><br />
会员A：【可用余额：500】。翻倍最高下注分数为：100，平倍最高下注分数为：500。<br />
会员B：【可用余额：5000】。翻倍最高下注额度为：1000，平倍最高下注额度为：5000。<br />
<br />
